/**
 * Un operador es un símbolo que le dice al compilador que realice una tarea específica.
 *  Por ejemplo, el operador de suma (+) le dice al compilador que sume dos números.
 * 
 */

main() {
  int a = 10 + 5; // El operador de suma (+) se utiliza para sumar dos números.
  int b =
      10 - 5; // El operador de resta (-) se utiliza para restar dos números.
  int c =
      10 *
      5; // El operador de multiplicación (*) se utiliza para multiplicar dos números.
  double d =
      10 /
      5; // El operador de división (/) se utiliza para dividir dos números.
  double e =
      10 %
      3; // El operador de módulo (%) se utiliza para obtener el resto de la división de dos números.
  e = -e; // El operador de negación (-) se utiliza para cambiar el signo de un número.
  int f =
      10 ~/
      3; // El operador de división entera (~/) se utiliza para dividir dos números y obtener el resultado como un entero.
  double g = 1;
  g++; // El operador de incremento (++) se utiliza para aumentar el valor de una variable en 1.
  g--; // El operador de decremento (--) se utiliza para disminuir el valor de una variable en 1.
  g +=
      5; // El operador de asignación compuesta (+=) se utiliza para sumar un valor a una variable y asignar el resultado a la misma variable.
  g -=
      5; // El operador de asignación compuesta (-=) se utiliza para restar un valor a una variable y asignar el resultado a la misma variable.
  g *=
      5; // El operador de asignación compuesta (*=) se utiliza para multiplicar un valor a una variable y asignar el resultado a la misma variable.
  g /=
      8; // El operador de asignación compuesta (/=) se utiliza para dividir un valor a una variable y asignar el resultado a la misma variable.
}
